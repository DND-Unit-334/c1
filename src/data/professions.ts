export const getDemandedProfessions = (): Profession[] => {
  return professions
    .filter(profession => profession.demandLevel >= 4)
    .sort((a, b) => b.demandLevel - a.demandLevel);
};

export const getWorkingProfessions = (): Profession[] => {
  return professions.filter(profession => profession.workingProfession);
};

export const getProfessionsByType = (type: string): Profession[] => {
  return professions.filter(profession => profession.type === type);
};

